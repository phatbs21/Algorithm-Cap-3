#define ll long long int
#define N 2942
const ll MOD = 1000000007;
int n, h;
int a[N];
ll f[N][N];
int main()
{
    cin >> n >> h;
    for (int i = 1; i <= n; ++i)
    {
        cin >> a[i];
        a[i] = h - a[i];
    }
    a[0] = 0;
    f[0][0] = 1;
    for (int i = 0; i <= n; ++i)
    {
        f[i + 1][a[i + 1]] = (f[i + 1][a[i + 1]] + f[i][a[i + 1]]) % MOD;
        f[i + 1][a[i + 1] - 1] = (f[i + 1][a[i + 1] - 1] + f[i][a[i + 1] - 1] * a[i + 1]) % MOD;
        f[i + 1][a[i + 1]] = (f[i + 1][a[i + 1]] + f[i][a[i + 1] - 1]) % MOD;
        f[i + 1][a[i + 1] - 1] = (f[i + 1][a[i + 1] - 1] + f[i][a[i + 1]] * a[i + 1]) % MOD;
    }
    cout << f[n + 1][0];
}
